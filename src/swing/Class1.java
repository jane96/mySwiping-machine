package swing;

public class Class1 {
	private String ooc;
	private String line;
	private String car;
	public Class1(){}
	
	public Class1(String ooc, String line, String car) {
		super();
		this.ooc = ooc;
		this.line = line;
		this.car = car;
	}

	public String getOoc() {
		return ooc;
	}
	public void setOoc(String ooc) {
		this.ooc = ooc;
	}
	public String getLine() {
		return line;
	}
	public void setLine(String line) {
		this.line = line;
	}
	public String getCar() {
		return car;
	}
	public void setCar(String car) {
		this.car = car;
	}
	
}
